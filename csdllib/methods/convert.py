#============================================================================== 
def ft2meters(ft):
    return 0.3048*float(ft)

#============================================================================== 
def meters2ft(m):
    return 3.28084*float(m)

#============================================================================== 
def mph2knots(mph):
    return 0.868976*float(mph)

#============================================================================== 
def mph2mps(mph):
    return 0.44704*float(mph)

#============================================================================== 
def mph2kmh(mph):
    return 1.60934*float(mph)

